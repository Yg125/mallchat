package org.yg.mallchat.common.websocket.service;

/**
 * @author yangang
 * @create 2025-01-14-下午9:45
 */
public interface LoginService {
    /**
     * 刷新token有效期
     *
     * @param token
     */
    void renewalTokenIfNecessary(String token);

    /**
     * 登录成功，获取token
     *
     * @param uid
     * @return 返回token
     */
    String login(Long uid);

    /**
     * 如果token有效，返回uid
     *
     * @param token
     * @return
     */
    Long getValidUid(String token);
}
